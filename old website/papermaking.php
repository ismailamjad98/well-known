                <b>Desirable Properties of Paper-making Felts:</b>
                <p>There are many kinds of paper grades such as newsprint paper, printing/information paper, sanitary paper, specialty paper, liner board and white paper-board. Each kind of paper is manufactured on the appropriate paper-making machine under the proper conditions, but the principles of manufacturing respective paper is more or less the same. Paper-making machines generally have forming, pressing and drying sections and paper-making felts are mainly used at the pressing section. The role of the pressing section is to receive wet paper web from the forming section by way of felt and to squeeze water from the wet paper web. Therefore, paper-making felts need to:</p>

                <ul>
                    <li>Function as conveyor belts to transfer wet paper web</li>
                    <li>Allow as much squeezing of water as possible out of wet paper web</li>
                    <li>Keep an even smooth surface of wet paper web</li>
                </ul>

                <b>Paper-making felts require the following properties:</b>
                <ul>
                    <li>Dewatering ability</li>
                    <li>Transferability of wet paper web</li>
                    <li>Runnability</li>
                    <li>Wear resistance and resistance to hair shedding</li>
                    <li>Compaction resistance</li>
                    <li>Paper surface smoothness</li>
                    <li>Break-in</li>
                    <li>Uniformity</li>
                    <li>Simple installation</li>
                </ul>



